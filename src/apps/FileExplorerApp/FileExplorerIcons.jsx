// Combined icon selector for items
export const ItemIcon = ({ item }) => {
  if (item.type === 'directory' || item.type === 'folder') {
    return <FolderIcon />;
  }
  
  const IconComponent = item.fileType && FileIcons[item.fileType] ? 
    FileIcons[item.fileType] : FileIcons.default;
  
  return <IconComponent />;
};